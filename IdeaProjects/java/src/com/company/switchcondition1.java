package com.company;

public class switchcondition1 {
    public static void main(String[]args){
        int day=2;
        switch (day){
            case 6:
            System.out.println("today is  saturday");
            break;
            case 7:
            System.out.println("today is sunday");
            break;
            default:
            System.out.println("looking forward to the weekend");
        }

    }
}
